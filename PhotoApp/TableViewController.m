- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"TableViewCell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
    
    cell.textLabel.text = [_tableArray objectAtIndex:indexPath.row];
    
    if (indexPath.row > _photoArray.count - 1) {
        cell.imageView.image = [UIImage imageNamed:@"Default.png"];
    }
    else{
    cell.imageView.image = [_photoArray objectAtIndex:indexPath.row];
    }
    
    NSLog(@"tableViewArray %@", _tableArray);
    NSLog(@"%i", indexPath.row);
    NSLog(@"object at indexpath for array: tableArray %@", [_tableArray objectAtIndex:indexPath.row]);
    
    return cell;
}

